package com.cesar.computers.obj;

public class Computer {

    private String name;
    private double price;
    private String description;

    public Computer(String name, double price, String description) {
        this.name = name;
        this.price = price;
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public String getDescription() {
        return description;
    }
}